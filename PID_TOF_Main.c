/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
	/* USER CODE BEGIN 1 */

	// PID Constants
	const double KP = 0.5;  // Proportional Constant  High Score (0.5)  // TODO: Make these defined
	const double KI = 0.5;  // Integral Constant     High Score (0.5)
	const double KD = 0.5;  // Derivative Constant  High Score (0.5)
	// PID Time
	uint32_t time1 = HAL_GetTick();  // ms value
	uint32_t time2;  // Latest time
	uint32_t deltaTime;
	// PID Percentage Conversions
	double travelPerc;  // Percentage of travel (100-200mm)
	double setpointPerc;  // Percentage of ADC (0-3.3V)
	double outputPerc;  // Percentage of DAC (1.105-2.865V)
	// PID Error
	double dac_error = 0;  // Instantaneous Error
	double lastError = 0;  // Previous Error
	double totalError = 0;  // Total Error
	double deltaError = 0;  // Change in Error
	// DAC Variables
	uint16_t dac_ch2_value = 0;  // DAC2 output value (bits)
	const uint16_t maxControl = 3555;  // Max value that DAC reaches
	const ushort minError = 0;  // Minimum error possible
	const uint16_t maxDifference = 2184; // Difference between max and min bit voltage values (1371-3555)
	// ADC Variables
	uint16_t rawSetpoint = 0;  // ADC2 bit values (Setpoint)
	double outputVoltage = 0;  // ADC1 Voltage (Output)
	double setpointVoltage = 0;  // ADC2 Voltage
	// TOF and UART variables
	long distance = 0;  // Distance read by sensor
	char msg[200];  // UART Buffer

	/* USER CODE END 1 */

	/* MCU Configuration--------------------------------------------------------*/

	/* Reset of all peripherals, Initializes the Flash interface and the Systick. */
	HAL_Init();

	/* USER CODE BEGIN Init */

	/* USER CODE END Init */

	/* Configure the system clock */
	SystemClock_Config();

	/* Configure the peripherals common clocks */
	PeriphCommonClock_Config();

	/* USER CODE BEGIN SysInit */

	/* USER CODE END SysInit */

	/* Initialize all configured peripherals */
	MX_GPIO_Init();
	MX_USART2_UART_Init();
	MX_ADC1_Init();
	MX_ADC2_Init();
	MX_DAC1_Init();
	MX_RTC_Init();
	/* USER CODE BEGIN 2 */
	MX_TOF_Init();
	/* Starts the specified DAC on specified channel */
	HAL_DAC_Start(&hdac1, DAC_CHANNEL_2);
	// Set value in DAC channel 2 Initially
	HAL_DAC_SetValue(&hdac1, DAC_CHANNEL_2, DAC_ALIGN_12B_R, dac_ch2_value);
	time1 = time(NULL);  // Assign time
	/* USER CODE END 2 */

	/* Infinite loop */
	/* USER CODE BEGIN WHILE */
	while (1)
	{
		/* USER CODE END WHILE */
		// Read both ADC channels
		//rawOut = ReadADCChannel(&hadc1);  // Reads DAC Output (Will be used in minor loop)
		rawSetpoint = ReadADCChannel(&hadc2);  // Reads input setpoint voltage
		distance = MX_TOF_Process();  // Get distance

		if ((100 < distance) && (distance < 200))  // Arbitrary distance for testing
		{
			travelPerc = ((double)distance - 100) / 100;  // Range is already 100mm just subract
		}

		// Convert digital values to analog
		setpointPerc = (double)rawSetpoint / 4095;  // Get percentage of setpoint
		setpointVoltage = (double)rawSetpoint * VOLTBIT;  // TODO: Change this to comply with TOF

		// For calculating Output values:
		// Get the percentage from setpoint, convert into bit values from 1.105-2.865V (1371-3555)

		// PID Control
		time2 = HAL_GetTick();  // Update to latest time (Might be wrong)
		deltaTime = time2 - time1;  // Find difference in time
		dac_error = setpointPerc - travelPerc;  // Calculate Error TODO: Error will be different
		deltaError = dac_error - lastError;  // Find change in error
		totalError += dac_error;  // Add to total error for integral term
		if (totalError <= minError) { totalError = minError; }  // Minimum value

		// Main PID Equation
		outputPerc = (KP * dac_error) + (KI * totalError) + ((KD/deltaTime) * deltaError);
		if (outputPerc > 100) { dac_ch2_value = maxControl; }
		dac_ch2_value = (outputPerc * maxDifference) + 1371;  // TODO: Check rounding and confirm equation is correct & cleanup random numbers (Calculated after PID eq)
		outputVoltage = dac_ch2_value * VOLTBIT;  // Convert output to analog
		// Set value in DAC channel 1
		HAL_DAC_SetValue(&hdac1, DAC_CHANNEL_2, DAC_ALIGN_12B_R, dac_ch2_value);
		lastError = dac_error;
		time1 = time2;


		// Convert to string and print
		sprintf(msg, "External Setpoint Voltage: %fV\t Output Voltage: %fV\t Distance: %ldmm\r\n", setpointVoltage, outputVoltage, distance);
		HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), HAL_MAX_DELAY);
		/* USER CODE BEGIN 3 */
	}
	/* USER CODE END 3 */
}

